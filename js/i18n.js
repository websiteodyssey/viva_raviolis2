/* ---------- Application ---------- */
const VIVA_LANGS = ["fr", "en", "zh", "it", "es"];
let VivaLang = (function () {
  let saved = null;
  try { saved = localStorage.getItem("viva_lang"); } catch (e) {}
  return VIVA_LANGS.includes(saved) ? saved : "fr";
})();
window.VivaLang = VivaLang;

const LANG_LABEL = { fr: "FR", en: "EN", zh: "中文", it: "IT", es: "ES" };

function applyI18n(lang) {
  const dict = I18N[lang] || I18N.fr;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] != null) el.innerHTML = dict[key];
  });
  // état actif des options + libellé du bouton courant
  document.querySelectorAll(".lang-btn").forEach(b =>
    b.classList.toggle("active", b.getAttribute("data-lang") === lang)
  );
  const lbl = document.querySelector("#langCurrent .lang-current-label");
  if (lbl) lbl.textContent = LANG_LABEL[lang] || lang.toUpperCase();
}

function setVivaLang(lang) {
  if (!VIVA_LANGS.includes(lang)) return;
  VivaLang = lang; window.VivaLang = lang;
  try { localStorage.setItem("viva_lang", lang); } catch (e) {}
  applyI18n(lang);
  // notifie la carte (menu-data.js) pour re-render des catégories
  document.dispatchEvent(new CustomEvent("viva:langchange", { detail: { lang } }));
}
window.setVivaLang = setVivaLang;
window.catLabel = function (cat) {
  if (VivaLang === "fr") return cat.fr;
  if (VivaLang === "zh") return cat.cn;
  return (CAT_I18N[cat.id] && CAT_I18N[cat.id][VivaLang]) || cat.fr;
};

/* Initialisation immédiate (les scripts sont en fin de body) */
applyI18n(VivaLang);

